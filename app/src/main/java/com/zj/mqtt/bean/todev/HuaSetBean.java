public class HuaSetBean {

    /**
     * hua : 120
     * sat : 254
     * time : 0
     */

    private int hua;
    private int sat;
    private int time;

    public int getHua() {
        return hua;
    }

    public void setHua(int hua) {
        this.hua = hua;
    }

    public int getSat() {
        return sat;
    }

    public void setSat(int sat) {
        this.sat = sat;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
